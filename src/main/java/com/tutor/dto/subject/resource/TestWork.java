package com.tutor.dto.subject.resource;

public class TestWork {

    private int id;
    private boolean passed;

    public TestWork() {
    }

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public boolean isPassed() {
        return passed;
    }
    public void setPassed(boolean passed) {
        this.passed = passed;
    }
}
